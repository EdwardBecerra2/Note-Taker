notes.delete('/:id', (req, res) => {
    const id = req.params.id;
    readFromFile('./db/db.json')
        .then((data) => JSON.parse(data))
        .then((json) => {
            const result = json.filter((note) => note.id !== id);
            writeFile('./db/db.json', result);
            res.json(`Item ${id} has been deleted ğŸ—‘ï¸`);
        });
});

